#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	char	*str1;
	char	*str2;
	size_t	i;

	i = 0;
	str1 = (char *)s1;
	str2 = (char *)s2;
	if (n == 0)
		return (0);
	while (*str1 == *str2 && ++i < n)
	{
		str1++;
		str2++;
	}
	return ((void *)(s1 + i) - (s2 + 1));
}
